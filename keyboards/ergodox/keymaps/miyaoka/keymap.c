void persistant_default_layer_set(uint16_t default_layer) {
  eeconfig_update_default_layer(default_layer);
  default_layer_set(default_layer);
}

void switch_layer (uint8_t layer, bool on) {
  if (on) {
    layer_on(layer);
  } else {
    layer_off(layer);
  }
};

void type_code(uint8_t keycode){
  register_code (keycode);
  unregister_code (keycode);
};

void set_eisu(void){
  type_code (KC_MHEN);
  type_code (KC_LANG2);
};

void set_kana(void){
  type_code (KC_HENK);
  type_code (KC_LANG1);
};

bool is_tap (keyrecord_t *record) {
  return hold_timers[record->event.key.row][record->event.key.col]
  && timer_elapsed (hold_timers[record->event.key.row][record->event.key.col]) < TAPPING_TERM;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  // record pressed timer
  if (record->event.pressed) {
    hold_timers[record->event.key.row][record->event.key.col] = timer_read();
  }

  switch (keycode) {
    //--layers--

    //layout
    case QWERTY:
      if (record->event.pressed) {
        layer_move(L_QWERTY);
      }
      return false;
      break;
    case QWDR:
      if (record->event.pressed) {
        layer_move(L_QWDR);
      }
      return false;
      break;
    case GAME:
      if (record->event.pressed) {
        layer_move(L_QWERTY);
        layer_on(L_GAME);
      }
      return false;
      break;

    //momentary layer
    case GAME2:
      switch_layer(L_GAME2, record->event.pressed);
      return false;
      break;
    case NAV:
      switch_layer(L_NAV, record->event.pressed);
      return false;
      break;
    case CONF:
      switch_layer(L_CONF, record->event.pressed);
      return false;
      break;

    //os
    case MAC:
      set_unicode_input_mode(UC_OSX);
      if (record->event.pressed) {
        persistant_default_layer_set(1UL<<L_MAC);
      }
      return false;
      break;
    case WIN:
      set_unicode_input_mode(UC_WINC);
      if (record->event.pressed) {
        persistant_default_layer_set(1UL<<L_WIN);
      }
      return false;
      break;
  }
  return true;
}

const macro_t *action_get_macro(keyrecord_t *record, uint8_t id, uint8_t opt)
{
  switch(id) {
    case T_EISU: {
      if (record->event.pressed) {
        add_mods(MOD_BIT(KC_LALT));
      } else {
        if (is_tap(record)) {
          del_mods(MOD_BIT(KC_LALT));
          set_eisu();
        } else {
          unregister_code(KC_LALT);
        }
      }
      break;
    }
    case T_KANA: {
      if (record->event.pressed) {
        layer_on(L_SYMB);
      } else {
        layer_off(L_SYMB);
        if (is_tap(record)) {
          set_kana();
        }
      }
      break;
    }
  }
  return MACRO_NONE;
};

// Runs just one time when the keyboard initializes.
void matrix_init_user(void) {
  set_unicode_input_mode(UC_OSX);
  persistant_default_layer_set(1UL<<L_MAC);
  layer_move(L_QWDR);
};
